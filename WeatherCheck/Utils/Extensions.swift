import UIKit

//MARK: - Double

extension Double {
    func toString() -> String {
        return "\(self)"
    }
    
    func toInt() -> Int {
        return Int(self)
    }
}

//MARK: - Int
extension Int {
    func toString() -> String {
        return "\(self)"
    }
}
